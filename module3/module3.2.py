hytti = input('Mikä hytin nimesi on? ')

if hytti == "LUX":
    print("Lux on yläkannen parvekkellinen hytti.")
elif hytti == "A":
    print("A on ikkunallinen hytti autokannen yläpuolella.")
elif hytti == "B":
    print("B on ikkunaton hytti autokannen yläpuolella.")
elif hytti == "C":
    print("C on ikkunaton hytti autokannen alapuolella.")
else:
    print("Mitä sää ukko höpiset")
